#include <iostream>

int main(){
	int length, width, height, area;

	length = 5;
	width = 6;
	height = 7;
	area = length * width * height;

	std::cout << "The length is: " << length << std::endl;
	std::cout << "The width is: " << width << std::endl;
	std::cout << "The height is: " << height << std::endl;
	std::cout << "The area is: " << area << std::endl;

	return 0;
}

